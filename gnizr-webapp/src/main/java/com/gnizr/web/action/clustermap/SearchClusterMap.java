package com.gnizr.web.action.clustermap;

import org.apache.log4j.Logger;

import com.gnizr.web.action.AbstractAction;

public class SearchClusterMap extends AbstractAction{

	private static final long serialVersionUID = -8399984707935489253L;
	private static final Logger logger = Logger.getLogger(SearchClusterMap.class);
	
	private String queryString;
	private String type;
	
	protected String go() throws Exception{
		logger.debug("SearchClusterMap.go(): queryString=" + queryString + ",type="+type);		
		return SUCCESS;
	}

	/**
	 * @return the queryString
	 */
	public String getQueryString() {
		return queryString;
	}

	/**
	 * @param queryString the queryString to set
	 */
	public void setQueryString(String queryString) {
		this.queryString = queryString;
	}

	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}

	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}



}
